#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [_sumbyyearArray count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"yearCell" forIndexPath:indexPath];
    
    // Configure the cell...
    SumbyYear *oneSumbyYear = [_sumbyyearArray objectAtIndex:[indexPath row]];
    NSString *money = [[oneSumbyYear money] stringValue];
    NSString *year = [[oneSumbyYear year] stringValue];
    [[cell textLabel] setText:year];
    [[cell detailTextLabel] setText:[@"$" stringByAppendingString:money]];
    
    return cell;
}

- (void)viewWillAppear:(BOOL)animated
{
    [super viewWillAppear:animated];
    _sumbyyearArray = [_dataSets getSumbyYearList];
    [self.tableView reloadData];
}

