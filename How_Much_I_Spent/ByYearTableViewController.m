#pragma mark - Navigation

// In a storyboard-based application, you will often want to do a little preparation before navigation
- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    // Get the new view controller using [segue destinationViewController].
    // Pass the selected object to the new view controller.
    if ([[segue identifier] isEqualToString:@"yeartomonth"]) {
        ByMonthTableViewController *byMonthTableViewController = [segue destinationViewController];
        [byMonthTableViewController setDataSets:_dataSets];
        NSIndexPath *indexPath = [[self tableView] indexPathForSelectedRow];
        SumbyYear *oneSumbyYear = [_sumbyyearArray objectAtIndex:[indexPath row]];
        [byMonthTableViewController setYear:[oneSumbyYear year]];
    }
}

